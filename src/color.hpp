template<typename T_component> struct Color {
	constexpr Color() :
			a_(0xFF), r_(0), g_(0), b_(0) {
	}

	constexpr Color(const Color& other) :
			a_(other.a_), r_(other.r_), g_(other.g_), b_(other.b_) {
	}

	constexpr Color(const T_component& a, const T_component& r, const T_component& g, const T_component& b) :
			a_(0xFF), r_(r), g_(g), b_(b) {
	}

	constexpr Color(const uint32_t& argb) :
			a_(0xFF), r_(argb >> 16), g_(argb >> 8), b_(argb) {
	}

	void operator=(const Color& other) {
		a_ = other.a_;
		r_ = other.r_;
		g_ = other.g_;
		b_ = other.b_;
	}

	T_component a_;
	T_component r_;
	T_component g_;
	T_component b_;
};

